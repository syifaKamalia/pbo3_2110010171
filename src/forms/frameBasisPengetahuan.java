    public void tampilDataBasisPengetahuan() {
        try {
            modelTabelBasisPengetahuan.getDataVector().removeAllElements();
            modelTabelBasisPengetahuan.fireTableDataChanged();
            
            PreparedStatement query = obj.konek.prepareStatement("SELECT * FROM basis_pengetahuan");
            ResultSet data = query.executeQuery();
            while (data.next()) {                
                Object[] line = new Object[5];
                line[0] = data.getString("kode_pengetahuan");
                line[1] = data.getString("kode_penyakit");
                line[2] = data.getString("kode_gejala");
                line[3] = data.getString("mb");
                line[4] = data.getString("md");
                modelTabelBasisPengetahuan.addRow(line);   
            }
            query.close();
            data.close();
        } catch (Exception e) {
        }
    }

