    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
           if (txtKdPengetahuan.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Kode pengetahuan belum di isi");
               txtKdPengetahuan.requestFocus();
           } else if (txtKdPenyakit.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Kode penyakit belum diisi");
               txtKdPenyakit.requestFocus();
           } else if (txtKdGejala.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Kode gejala belum diisi");
               txtKdGejala.requestFocus();
           } else if (txtMb.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Mb belum diisi");
               txtMb.requestFocus();
           } else if (txtMd.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Md belum diisi");
               txtMd.requestFocus();
           } else {
               String sqlCari = "SELECT * FROM basis_pengetahuan WHERE kode_pengetahuan ='"+txtKdPengetahuan.getText()+"'";
               ResultSet cari = obj.konek.createStatement().executeQuery(sqlCari);
               if (cari.next()){
                    JOptionPane.showMessageDialog(this, "NPM Sudah Terdaftar");
                         txtKdPenyakit.setText(cari.getString(2));
                         txtKdGejala.setText(cari.getString(3));
                         txtMb.setText(cari.getString(4));
                         txtMd.setText(cari.getString(5));
               } else {
                    String sql = "INSERT INTO basis_pengetahuan VALUE(?, ?, ?, ?, ?)";
                    PreparedStatement simpan = obj.konek.prepareStatement(sql);
                    simpan.setString(1, txtKdPengetahuan.getText());
                    simpan.setString(2, txtKdPenyakit.getText());
                    simpan.setString(3, txtKdGejala.getText());
                    simpan.setString(4, txtMb.getText());
                    simpan.setString(5, txtMd.getText());
                    simpan.executeUpdate();
                    tampilDataBasisPengetahuan();
                    
                    JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
               }
         
               
               
               JOptionPane.showMessageDialog(this, "data berhasil disimpan");
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

