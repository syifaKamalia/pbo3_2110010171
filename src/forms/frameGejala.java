    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        try {
            if (
                    (txtKdGejala.getText().isEmpty()) &&
                    (txtNmGejala.getText().isEmpty()) &&
                    (txtKdKategori.getText().isEmpty()) &&
                    (txtIdKondisi.getText().isEmpty())
               ) {
                JOptionPane.showMessageDialog(this, "Silahkan pilih data terlebih dahulu!");
            } else {
                String sql= "UPDATE gejala SET nama_gejala=?, kd_kategori=?, id_kondisi=?  WHERE kode_gejala=?";
                PreparedStatement ubah = obj.konek.prepareStatement(sql);
                ubah.setString(1, txtNmGejala.getText());
                ubah.setString(2, txtKdKategori.getText());
                ubah.setString(3, txtIdKondisi.getText());
                ubah.setString(4, txtKdGejala.getText());
                ubah.executeUpdate();
                tampilDataGejala();
                JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton3ActionPerformed

