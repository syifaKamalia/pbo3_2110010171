    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
           if (txtKdGejala.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Kode Gejala belum di isi");
               txtKdGejala.requestFocus();
           } else if (txtNmGejala.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Nama Gejala belum diisi");
               txtNmGejala.requestFocus();
           } else if (txtKdKategori.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Kode Kategori belum diisi");
               txtKdKategori.requestFocus();
           } else if (txtIdKondisi.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Id Kondisi belum diisi");
               txtIdKondisi.requestFocus();
           } else {
               String sqlCari = "SELECT * FROM gejala WHERE kode_gejala ='"+txtKdGejala.getText()+"'";
               ResultSet cari = obj.konek.createStatement().executeQuery(sqlCari);
               if (cari.next()){
                    JOptionPane.showMessageDialog(this, "Kode Gejala Sudah Terdaftar");
                         txtNmGejala.setText(cari.getString(2));
                         txtKdKategori.setText(cari.getString(3));
                         txtIdKondisi.setText(cari.getString(4));
               } else {
                    String sql = "INSERT INTO gejala VALUE(?, ?, ?, ?)";
                    PreparedStatement simpan = obj.konek.prepareStatement(sql);
                    simpan.setString(1, txtKdGejala.getText());
                    simpan.setString(2, txtNmGejala.getText());
                    simpan.setString(3, txtKdKategori.getText());
                    simpan.setString(4, txtIdKondisi.getText());
                    simpan.executeUpdate();
                    tampilDataGejala();
                    
                    JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
               }
               JOptionPane.showMessageDialog(this, "data berhasil disimpan");
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

