    public void tampilDataGejala() {
        try {
            modelTabelGejala.getDataVector().removeAllElements();
            modelTabelGejala.fireTableDataChanged();
            
            PreparedStatement query = obj.konek.prepareStatement("SELECT * FROM gejala");
            ResultSet data = query.executeQuery();
            while (data.next()) {                
                Object[] baris = new Object[4];
                baris[0] = data.getString("kode_gejala");
                baris[1] = data.getString("nama_gejala");
                baris[2] = data.getString("kd_kategori");
                baris[3] = data.getString("id_kondisi");
                modelTabelGejala.addRow(baris);   
            }
            query.close();
            data.close();
        } catch (Exception e) {
        }
    }    

