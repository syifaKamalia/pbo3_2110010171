    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
           if (txtIdHasil.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Id Hasil belum di isi");
               txtIdHasil.requestFocus();
           } else if (txtTgl.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Tanggal belum diisi");
               txtTgl.requestFocus();
           } else if (txtKdPenyakit.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Kode Penyakit belum diisi");
               txtKdPenyakit.requestFocus();
           } else if (txtKdGejala.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Kode Gejala belum diisi");
               txtKdGejala.requestFocus();
           } else if (txtId.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "ID belum diisi");
               txtId.requestFocus();
           } else if (txtHasilNilai.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Hasil Nilai Nilai belum diisi");
               txtHasilNilai.requestFocus();
           } else if (txtUser.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "User belum diisi");
               txtUser.requestFocus();
           } else {
               String sqlCari = "SELECT * FROM hasil WHERE id_hasil ='"+txtIdHasil.getText()+"'";
               ResultSet cari = obj.konek.createStatement().executeQuery(sqlCari);
               if (cari.next()){
                    JOptionPane.showMessageDialog(this, "ID Hasil Sudah Terdaftar");
                         txtTgl.setText(cari.getString(2));
                         txtKdPenyakit.setText(cari.getString(3));
                         txtKdGejala.setText(cari.getString(4));
                         txtId.setText(cari.getString(5));
                         txtHasilNilai.setText(cari.getString(6));
                         txtUser.setText(cari.getString(7));
               } else {
                    String sql = "INSERT INTO hasil VALUE(?, ?, ?, ?, ?, ?, ?)";
                    PreparedStatement simpan = obj.konek.prepareStatement(sql);
                    simpan.setString(1, txtIdHasil.getText());
                    simpan.setString(2, txtTgl.getText());
                    simpan.setString(3, txtKdPenyakit.getText());
                    simpan.setString(4, txtKdGejala.getText());
                    simpan.setString(5, txtId.getText());
                    simpan.setString(6, txtHasilNilai.getText());
                    simpan.setString(7, txtUser.getText());
                    simpan.executeUpdate();
                    tampilDataHasil();
                    
                    JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
               }
               JOptionPane.showMessageDialog(this, "data berhasil disimpan");
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

