    public void tampilDataHasil() {
        try {
            modelTabelHasil.getDataVector().removeAllElements();
            modelTabelHasil.fireTableDataChanged();
            
            PreparedStatement query = obj.konek.prepareStatement("SELECT * FROM hasil");
            ResultSet data = query.executeQuery();
            while (data.next()) {                
                Object[] line = new Object[7];
                line[0] = data.getString("id_hasil");
                line[1] = data.getString("tanggal");
                line[2] = data.getString("kode_penyakit");
                line[3] = data.getString("kode_gejala");
                line[4] = data.getString("id");
                line[5] = data.getString("hasil_nilai");
                line[6] = data.getString("user");
                modelTabelHasil.addRow(line);   
            }
            query.close();
            data.close();
        } catch (Exception e) {
        }
    }

